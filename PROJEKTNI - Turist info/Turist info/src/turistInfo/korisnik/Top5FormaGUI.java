    public void popuniTabeluTop5(ArrayList<Turista> top5, JTable tabelaTop5) {

        //sviDogadjaji = dogadjaji;
        DefaultTableCellRenderer centriranje = new DefaultTableCellRenderer();
        centriranje.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
        DefaultTableCellRenderer centriranjeLijevo = new DefaultTableCellRenderer();
        centriranjeLijevo.setHorizontalAlignment(DefaultTableCellRenderer.LEFT);
        DefaultTableCellRenderer centriranjeDesno = new DefaultTableCellRenderer();
        centriranjeDesno.setHorizontalAlignment(DefaultTableCellRenderer.RIGHT);
        tabelaTop5.getColumnModel().getColumn(0).setCellRenderer(centriranje);
        tabelaTop5.getColumnModel().getColumn(1).setCellRenderer(centriranjeLijevo); 
        tabelaTop5.getColumnModel().getColumn(2).setCellRenderer(centriranjeDesno);
        tabelaTop5.getColumnModel().getColumn(3).setCellRenderer(centriranjeDesno);
        //brisanje tabele
        int rowCount = modelTabelaTop5.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            modelTabelaTop5.removeRow(i);
        }

        int redniBroj = 1;
        for (Turista turista : top5) {
           // System.out.println("==========================Turista iz top 5: " + turista.getIme());
            String imeTuriste = turista.getIme();
            int brojPosjecenihAtrakcija = turista.getBrojPosjecenihMjesta();
            double postotakPosjecenosti = (((double) turista.getBrojPosjecenihMjesta() / (double) ukupnoTuristickihAtrakcija) * 100.0);
            DecimalFormat df = new DecimalFormat("0.00");

            //System.out.println("==========================POSTOTAK: " + df.format(postotakPosjecenosti));
            modelTabelaTop5.addRow(new Object[]{redniBroj, imeTuriste, brojPosjecenihAtrakcija, df.format(postotakPosjecenosti)});

            redniBroj++;
        }

    }

