    private void dugmeLetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dugmeLetakActionPerformed
        int selektovaniTurista = tabelaTop5.getSelectedRow();
        if(selektovaniTurista > -1){
        String letak = "";
        Turista odabraniTurista = top5.get(selektovaniTurista);
        File fajlLetka;
        BufferedReader citaj;
        try {

            File folder = odabraniTurista.getFajl();//folder turiste
            File[] listOfFiles = folder.listFiles();
            boolean postojiLetak = false;
            
            for (File file : listOfFiles) {
                if (file.isFile()) {
                    if (file.getName().endsWith(".txt")) {
                        postojiLetak = true;
                        fajlLetka = file;
                        citaj = new BufferedReader(new FileReader(fajlLetka));
                        StringBuilder sb = new StringBuilder();
                        String line = citaj.readLine();
                        while (line != null) {
                            sb.append(line);
                            sb.append(System.lineSeparator());
                            line = citaj.readLine();
                        }
                        letak = sb.toString();
                        JTextArea textArea = new JTextArea(11, 45);
                        textArea.setLineWrap(true);
                        textArea.setText(letak);
                        textArea.setEditable(false);
                        // wrap a scrollpane around it
                        JScrollPane scrollPane = new JScrollPane(textArea);
                        // display them in a message dialog
                        JOptionPane.showMessageDialog(null, scrollPane, "Letak", JOptionPane.INFORMATION_MESSAGE);
                        break;
                    }
                }
            }
            
            
           if(postojiLetak == false){
            JOptionPane.showMessageDialog(null, "Turista nema sakupljenih letaka!");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        }
        else{
            JOptionPane.showMessageDialog(null, "Nije selektovan ni jedan turista! ");
        }
    }//GEN-LAST:event_dugmeLetakActionPerformed

